    private void btnCloseTabActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCloseTabActionPerformed
        if(tbtContent.getComponentCount() <= 0){
            
            JOptionPane.showMessageDialog(this,"Caballo No hay ningun Editor abierto");
            return;
        } 
        int option = JOptionPane.showInternalConfirmDialog(null,"Are you sure to delete this tab",
                "Confirmation Dialog", JOptionPane.YES_NO_OPTION,
                JOptionPane.WARNING_MESSAGE);
        
        if(option == 1){
           return;
        }
        
        int index = tbtContent.getSelectedIndex();
        tbtContent.remove(index);
        
        
        
        
       
    }//GEN-LAST:event_btnCloseTabActionPerformed

