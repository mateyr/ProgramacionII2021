 private void EscribeFichero(PnlTextEditor pnltexteditor){
        
       String texto = pnltexteditor.getTxtAEditor().getText();
       //"C:/Users/LENOVO/Documents/NetBeansProjects/TextEditor/Texto.txt"
       FileWriter flwriter = null;
		try {
                        String archivoname = JOptionPane.showInputDialog("Ingrese nombre con que desea guardar el archivo:");
			//crea el flujo para escribir en el archivo
			flwriter = new FileWriter("C:\\Users\\LENOVO\\Documents\\NetBeansProjects\\TextEditor\\src\\main\\resources\\Archivos\\"+ archivoname +".txt");  
			//crea un buffer o flujo intermedio antes de escribir directamente en el archivo
			BufferedWriter bfwriter = new BufferedWriter(flwriter);
                        
                        for (int i = 0; i <texto.length(); i++){
                            bfwriter.write(texto.charAt(i));
                        }
                        		
			//cierra el buffer intermedio
			bfwriter.close();
			
 
		} catch (IOException e) {
			e.printStackTrace();
		} finally {
			if (flwriter != null) {
				try {//cierra el flujo principal
					flwriter.close();
				} catch (IOException e) {
					e.printStackTrace();
				}
			}
		}
        
    }
 
  
 
 
 
 
 


    
    
    
    
